void main() {
// adiciona
  double valor = 2;
  valor += 1;
  print(valor);

// tira
  valor -= 1;
  print(valor);

// multiplica
  valor *= 20;
  print(valor);

// divide
  valor /= 2;
  print(valor);

// int divisão
  var valor2 = 10;
  valor2 ~/= 4;
  print(valor2);

// resto divisão
  valor %= 2;
  print(valor);

// <<=
//>>=
//&=
//|=
}
