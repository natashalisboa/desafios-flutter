void main() {
// add
  var soma = 3 + 2;
  print(soma);

// subtract
  var subtracao = 3 - 2;
  print(subtracao);

// unary minus, also known as negation
  var inversao = -(-3);
  print(inversao);

// multiply
  var multiplicacao = 3 * 2;
  print(multiplicacao);

// divide
  var divisao = 3 / 2;
  print(divisao);

// divide, returning an integer result
  var int_divisao = 10 ~/ 3;
  print(int_divisao);

// get the remainder of an integer division (modulo)
  var resto_div = 3 % 2;
  print(resto_div);
}
