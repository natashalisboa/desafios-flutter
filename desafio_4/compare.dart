void main() {
// equal
  var num1 = 5;
  var a = num1 == 10;
  print(a);

// not equal
  var num2 = 3;
  var b = num2 != 5;
  print(b);

// greater than
  var num3 = 10;
  var c = num3 > 12;
  print(c);

// less than
  var num4 = 10;
  var d = num4 < 12;
  print(d);

// greater than or equal to
  int num5 = 10;
  bool e = num5 >= 12;
  print(e);

// less than or equal to
  var num6 = 10;
  var f = num6 <= 12;
  print(f);
}
